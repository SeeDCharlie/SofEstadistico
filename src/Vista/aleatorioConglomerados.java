    private void jtNumConglomeradosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jtNumConglomeradosActionPerformed
        // TODO add your handling code here:+
        if (Globales.verificarSiEsNum(jtNumConglomerados.getText())) {
            numConglomerados = Integer.parseInt(jtNumConglomerados.getText());
            if (numConglomerados < 0) {
                JOptionPane.showMessageDialog(this, "El numero de Conglomerados no puede ser negativo");
            } else {
                if (numConglomerados > control.getConsulta().getn()) {
                    JOptionPane.showMessageDialog(this, "El numero de Conglomerados no puede ser mayor que el de la Muestra");
                } else {
                    DefaultTableModel def = (DefaultTableModel) jtGrupos.getModel();
                    if (def.getRowCount() > 0) {
                        ((DefaultListModel) jlCaracteristicas.getModel()).removeAllElements();
                    }
                    def.setColumnCount(0);
                    for (int i = 0; i < numConglomerados; i++) {
                        def.addColumn("Grup. " + (i + 1));
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "No has digitado un Numero de Conglomerados");
        }

    }//GEN-LAST:event_jtNumConglomeradosActionPerformed

    private void jtIntervalosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jtIntervalosActionPerformed
        // TODO add your handling code here:

        if (jlCaracteristicas.getModel().getSize() < numConglomerados) {
            if (Globales.verificarConglomerado(jtIntervalos.getText())) {
                int[] inter = new int[2];
                inter[0] = Integer.parseInt(jtIntervalos.getText().split("-")[0]);
                inter[1] = Integer.parseInt(jtIntervalos.getText().split("-")[1]);
                if (inter[0] > control.getConsulta().getN() || inter[1] > control.getConsulta().getN() || inter[0] < 0 || inter[1] < 0) {
                    JOptionPane.showMessageDialog(this, "En este intervalo hay elementos fuera de la poblacion");
                } else {
                    modelListaCong.addElement("[" + jtIntervalos.getText() + "]");
                    jlCaracteristicas.setModel(modelListaCong);
                    jtGrupos.setModel(control.agregarConglomerado(jtGrupos, inter, jlCaracteristicas.getModel().getSize() - 1));
                }
            } else {
                JOptionPane.showMessageDialog(this, "El Intervalo Fue Mal Escrito. Digite numeros positivos dentro del intervalo [1-N]");
            }
        } else {
            JOptionPane.showMessageDialog(this, "El Numero de Conglomerados ya Fue Escogito");
        }
    }//GEN-LAST:event_jtIntervalosActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (jlCaracteristicas.getModel().getSize() == numConglomerados) {
            control.aleatorioConglomerados(jtGrupos);
            jtMuestra.setModel(Globales.cargarDatosJtable(control.getConsulta().getnDatos(), control.getConsulta().getElementos()));
            jtMuestra.getColumnModel().getColumn(0).setPreferredWidth(30);
        } else {
            JOptionPane.showMessageDialog(this, "NO has terminado de Ingresar tus Grupos");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

