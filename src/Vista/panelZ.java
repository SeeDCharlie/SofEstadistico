    private void jbOkUnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbOkUnoActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            if (jtPuno.getText().equals("") && jtPdos.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "No has digitado ningun Valor");
            } else {
                if (jtPuno.getText().equals("")) {
                    if (Globales.verificarNumoDouble(jtPdos.getText())) {
                        double numero = (Double.parseDouble(jtPdos.getText()));
                        this.jtPorcenUno.setText(String.format("%.2f", control.calcularZ(String.format("%.2f", numero).replace(",", "."), (String) jcDos.getSelectedItem()) * 100).replace(",", "."));
                    } else {
                        JOptionPane.showMessageDialog(this, "No es un numero!");
                    }
                } else {
                    if (jtPdos.getText().equals("")) {
                        if (Globales.verificarDouble(jtPuno.getText())) {
                            double numero = Double.parseDouble(jtPuno.getText());
                            if (((String) jcUno.getSelectedItem()).equals("<")) {
                                this.jtPorcenUno.setText(String.format("%.2f", control.calcularZ(String.format("%.2f", numero).replace(",", "."), ">") * 100).replace(",", "."));
                            } else {
                                this.jtPorcenUno.setText(String.format("%.2f", control.calcularZ(String.format("%.2f", numero).replace(",", "."), "<") * 100).replace(",", "."));
                            }
                        } else {
                            JOptionPane.showMessageDialog(this, "No es un numero correcto");
                        }
                    }
                }
            }
            if (!jtPuno.getText().equals("") && !jtPdos.getText().equals("")) {
                if (Globales.verificarDouble(jtPuno.getText()) && Globales.verificarDouble(jtPdos.getText())) {
                    double numero = Double.parseDouble(jtPuno.getText());
                    double numeroDos = Double.parseDouble(jtPdos.getText());
                    double resultado;
                    resultado = 100 * control.calcularZporIntervalo(numero, numeroDos, (String) jcUno.getSelectedItem(), (String) jcDos.getSelectedItem(), String.format("%.2f", numero).replace(",", "."), String.format("%.2f", numeroDos).replace(",", "."));
                    jtPorcenUno.setText(String.format("%.2f", resultado).replace(",", "."));
                }
            }
        } catch (IOException ex) {
            Logger.getLogger(panelZ.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbOkUnoActionPerformed

