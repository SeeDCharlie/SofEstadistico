    private void jbCalcularUnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCalcularUnoActionPerformed
        // TODO add your handling code here:
        if (jrProporcionMu.isSelected()) {
            if (jtP.getText().equals("") || jtPgorro.getText().equals("") || jtn.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Hay Campos Vacios !!!");
            } else {
                if (Globales.verificarNumoDouble(jtP.getText()) && Globales.verificarNumoDouble(jtPgorro.getText()) && Globales.verificarNumoDouble(jtn.getText())) {
                    double n = Double.parseDouble(jtn.getText());
                    if (n >= 100) {

                        double p = Double.parseDouble(jtP.getText());
                        double pG = Double.parseDouble(jtPgorro.getText());
                        if (p >= 0 && p <= 100 && pG >= 0 && pG <= 100) {
                            if (p >= 1 && p <= 100) {
                                p = p / 100;
                            }
                            if (pG >= 1 && pG <= 100) {
                                pG = pG / 100;
                            }
                            double resultado = ((pG - p) / Math.sqrt(((p * (1 - p)) / n)));
                            if (jtPuno.getText().equals("")) {
                                jtPuno.setText(String.format("%.2f", resultado).replace(",", "."));
                            } else {
                                if (jtPdos.getText().equals("")) {
                                    jtPdos.setText(String.format("%.2f", resultado).replace(",", "."));
                                } else {
                                    JOptionPane.showMessageDialog(this, "Los Campos Azules(z) Estan Llenos  !!!!! ");
                                }
                            }
                        } else {
                            JOptionPane.showMessageDialog(this, "Digite Valores de porcentajes no  mayores al 100 % & que no sean Negativos");
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "El tamaño de la muestra deber mayor o igual a 100");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Los Datos No son Validos");
                }
            }
        }
        if (jrDistriX.isSelected()) {
            if (jtXbarra.getText().equals("") || jtMiuUno.getText().equals("") || jtSigmaUno.getText().equals("") || jtn.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Hay Campos Vacios !!!");
            } else {
                if (Globales.verificarNumoDouble(jtXbarra.getText().replace(",", ".")) && Globales.verificarNumoDouble(jtMiuUno.getText().replace(",", ".")) && Globales.verificarNumoDouble(jtSigmaUno.getText().replace(",", ".")) && Globales.verificarNumoDouble(jtn.getText().replace(",", "."))) {
                    int n = Integer.parseInt(jtn.getText());
                    if (n >= 30) {
                        double xbar = Double.parseDouble(jtXbarra.getText().replace(",", "."));
                        double miu = Double.parseDouble(jtMiuUno.getText().replace(",", "."));
                        double sig = Double.parseDouble(jtSigmaUno.getText().replace(",", "."));
                        if (sig > 0) {
                            double result = ((xbar - miu) / (sig / Math.sqrt(n)));
                            if (jtPuno.getText().equals("")) {
                                jtPuno.setText(String.format("%.2f", result).replace(",", "."));
                            } else {
                                if (jtPdos.getText().equals("")) {
                                    jtPdos.setText(String.format("%.2f", result).replace(",", "."));
                                } else {
                                    JOptionPane.showMessageDialog(this, "Los Campos Azules Estan Llenos!!!!");
                                }
                            }
                        } else {
                            JOptionPane.showMessageDialog(this, "Sigma no puede ser negativa !!!");
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "El tamaño de la muestra debe ser mayor o igual a 30");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Los Datos No son Validos");
                }
            }
        }
    }//GEN-LAST:event_jbCalcularUnoActionPerformed

    private void jbCalcularKoZActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCalcularKoZActionPerformed
        // TODO add your handling code here:
        if (jrHallarK.isSelected()) {
            if (jtZuno.getText().equals("") || jtMiu.getText().equals("") || jtSigma.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Hay Campos vacios !!");
            } else {
                if (Globales.verificarNumoDouble(jtMiu.getText()) && Globales.verificarNumoDouble(jtZuno.getText()) && Globales.verificarNumoDouble(jtSigma.getText())) {
                    if (Double.parseDouble(jtSigma.getText()) > 0) {
                        double resultado = ((Double.parseDouble(jtZuno.getText())) * Double.parseDouble(jtSigma.getText())) + Double.parseDouble(jtMiu.getText());
                        jtK.setText(String.format("%.2f", resultado).replace(",", "."));
                    } else {
                        JOptionPane.showMessageDialog(this, " σ No pueden ser Negativa !!!!");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Los datos no son correctos !!");
                }
            }
        }
        if (jrDarValorZ.isSelected()) {
            if (jtK.getText().equals("") && jtMiu.getText().equals("") && jtSigma.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Hay Campos Vacios !!");
            } else {
                if (Globales.verificarNumoDouble(jtK.getText()) && Globales.verificarNumoDouble(jtMiu.getText()) && Globales.verificarNumoDouble(jtSigma.getText())) {
                    double miu = Double.parseDouble(jtMiu.getText());
                    double sigm = Double.parseDouble(jtSigma.getText());
                    double ksito = Double.parseDouble(jtK.getText());
                    double result = (ksito - miu) / sigm;
                    if ( sigm > 0) {
                        if (jtZuno.getText().equals("")) {
                            jtZuno.setText(String.format("%.2f", result).replace(",", "."));
                        } else {
                            if (jtZdos.getText().equals("")) {
                                jtZdos.setText(String.format("%.2f", result).replace(",", "."));
                            } else {
                                JOptionPane.showMessageDialog(this, "Los Espacion En Azul estan llenos !!!");
                            }
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "σ No pueden ser Negativa !!!!");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Los Datos NO Son Correctos !!!!");
                }
            }
        }
    }//GEN-LAST:event_jbCalcularKoZActionPerformed

